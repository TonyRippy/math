func TestSieve(t *testing.T) {
	for j := 0; j < 2; j++ { // one Sieve run should not affect others...
		s := NewSieve()
		for i := 0; i < 1000; i++ {
			want := FirstThousandPrimes[i]
			if got := s.Next(); got != want {
				t.Errorf("Prime[%d]: want %d, got %d", i, want, got)
			}
		}
	}
}

var result uint64

// A becnhmark that times the result of generating the first N primes
func benchmarkSieveN(b *testing.B, n int) {
	var p uint64
	for i := 0; i < b.N; i++ {
		s := NewSieve()
		for j := 0; j < n; j++ {
			p = s.Next()
		}
	}
	result = p
}

